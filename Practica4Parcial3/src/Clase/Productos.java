    private void mnuAAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnuAAgregarActionPerformed
        // TODO add your handling code here:
        if (cbxCategorias.getSelectedItem() == "Eligue un producto") {
            JOptionPane.showMessageDialog(null, "Elige una opcion de la categoria");
        } else {
            if (cbxCategorias.getSelectedItem() == "Carnes") {
                String tipo = (String) cbxCategorias.getSelectedItem();
                lblTipo.setText(tipo);
                String carnes = (String) cbxCarnes.getSelectedItem();
                lblDescripcion.setText(carnes);
                String descuentos = (String) cbxDecuentos.getSelectedItem();
                lblUnidades.setText(descuentos);
                mnuAAgregar.setEnabled(false);
                mnmAComprar.setEnabled(true);
                txtPrecio.setEnabled(true);
            }
            if (cbxCategorias.getSelectedItem() == "Abarrotes") {
                String tipo = (String) cbxCategorias.getSelectedItem();
                lblTipo.setText(tipo);
                String aborrotes = (String) cbxAbarrotes.getSelectedItem();
                lblDescripcion.setText(aborrotes);
                String iva = (String) cbxIva.getSelectedItem();
                lblUnidades.setText(iva);
                mnuAAgregar.setEnabled(false);
                mnmAComprar.setEnabled(true);
                txtPrecio.setEnabled(true);
            }
            if (cbxCategorias.getSelectedItem() == "Carnes frias") {
                String tipo = (String) cbxCarnesFrias.getSelectedItem();
                lblTipo.setText(tipo);
                String carnes_frias = (String) cbxCarnesFrias.getSelectedItem();
                lblDescripcion.setText(carnes_frias);
                String unidades = (String) cbxUnidades.getSelectedItem();
                lblUnidades.setText(unidades);
                mnuAAgregar.setEnabled(false);
                mnmAComprar.setEnabled(true);
                txtPrecio.setEnabled(true);
                txtPeso.setEnabled(true);
            }
        }
    }//GEN-LAST:event_mnuAAgregarActionPerformed

    private void mnmAComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnmAComprarActionPerformed
        // TODO add your handling code here:
        if (txtPrecio.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Escribe el precio del producto");
        } else {
            if (cbxCategorias.getSelectedItem() == "Carnes") {
                int precio = Integer.parseInt(txtPrecio.getText());
                String de = (String) cbxDecuentos.getSelectedItem();
                de = de.substring(0, de.length() - 1);
                float st = precio - (precio * (Float.parseFloat(de) / 100));
                lblSubtotal.setText(Float.toString(st));
            }
            if (cbxCategorias.getSelectedItem() == "Abarrotes") {
                int precio = Integer.parseInt(txtPrecio.getText());
                String de = (String) cbxIva.getSelectedItem();
                de = de.substring(0, de.length() - 1);
                float st = precio + (precio * (Float.parseFloat(de) / 100));
                lblSubtotal.setText(Float.toString(st));
            }
            if (cbxCategorias.getSelectedItem() == "Carnes frias") {
                if (txtPeso.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Agrega una cantidad o peso del producto");
                } else {
                    int precio = Integer.parseInt(txtPrecio.getText());
                    String de = (String) cbxUnidades.getSelectedItem();
                    de = de.substring(0, de.length() - 1);
                    float peso = Float.parseFloat(txtPeso.getText());
                    float st = precio * peso;
                    lblSubtotal.setText(Float.toString(st));
                }
            }
        }
    }//GEN-LAST:event_mnmAComprarActionPerformed

